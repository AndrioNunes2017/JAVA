public class Adc_palavra {
    private String per;
    private String res;

    public Adc_palavra() {
    }

    public Adc_palavra(String per, String res) {
        this.per = per;
        this.res = res;
    }

    public String getPer() {
        return per;
    }

    public void setPer(String per) {
        this.per = per;
    }

    public String getRes() {
        return res;
    }

    public void setRes(String res) {
        this.res = res;
    }
    
   
    
}
