    private void abrirTXT(){ 
        File file = new File("ArquivosTXT/"+nome.getText()+".txt");
        FileInputStream fis = null;
        String texto = "";
        
         try {
            fis = new FileInputStream(file);
            int content;
            while ((content = fis.read()) != -1) {
                texto += (char) content;
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (fis != null) {
                    fis.close();
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
                  
        textArea.setText(texto);    
    }
    
    private void salvarTXT() throws IOException{
        Writer out = new FileWriter("ArquivosTXT/"+nome.getText()+".txt");
        //limpa
        out.write("");
        out.flush();
        
        out.write(textArea.getText());
        out.flush();
        out.close();
    }    
    
     private void limpar(){
        textArea.setText("");
        nome.setText("");
    }
    
